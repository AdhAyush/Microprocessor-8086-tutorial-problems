    .MODEL SMALL
    .STACK 32
    .DATA

MAX DB 10
ACT DB ?
STRING DB 11 DUP ('$')

    .CODE 
MAIN PROC FAR
    MOV AX,@DATA
    MOV DS,AX

;INPUT STRING
    MOV AH, 0AH
    LEA DX, MAX
    INT 21H
;PROCESS STRING
    LEA DX, STRING
    MOV CL, ACT
    MOV CH, 00h

L1: CMP BYTE PTR [BX], 41H
    JB SKIP
    CMP BYTE PTR [BX], 5AH
    JA SKIP
    ADD BYTE PTR [BX], 20H
SKIP: INC BX
    LOOP L1

;FORMAT THE DISPLAY 
    MOV AH, 06H
    MOV AL, 00h
    MOV BH, 0F0H
    MOV CH, 3
    MOV CL, 10
    MOV DH, 21
    MOV DL, 70
    INT 10H

;SET THE CURSOR
    MOV AH, 02H
    MOV BH, 00h
    MOV DH, 21
    MOV DL, 10
    INT 10H

;DISPLAY THE STRING
    MOV AH, 09H
    LEA DX, STRING
    INT 21H

;EXIT
    MOV AH, 4CH
    INT 21H

MAIN ENDP
    END MAIN