    .MODEL SMALL
    .STACK 32
    .DATA
MAX_LEN DB 9
ACT_LEN DB ?
STRING DB 10 DUP("$")
    .CODE

MAIN PROC FAR
    MOV AX, @DATA
    MOV DS, AX

    MOV DX, OFFSET MAX_LEN
    MOV AH, 0Ah
    INT 21h

    ;FORMAT THE DISPLAY
    MOV AH, 06H
    MOV AL, 00H
    MOV BH, 7FH
    MOV CX, 00H
    MOV DX, 184FH
    INT 10H

    ;SET THE CURSOR POSITIONAT CENTER
    MOV AH, 02H
    MOV BH, 00H
    MOV DH, 12
    MOV DL, 40
    INT 10H

    ;DISPLAY THE STRING
    MOV AH, 09H
    MOV DX, OFFSET STRING
    INT 21H

    MOV AH, 4CH
    INT 21H

MAIN ENDP
    END MAIN

